/*
escape 문자

\n : 라인변경
\t : tab 크기 만큼 이동
\b : 왼쪽으로 한칸 이동
\r : 해당 라인의 처음으로 이동

*/

int main() {

	printf("오늘도 즐거운 하루 되세요~ ^^\n");
	printf("날씨가 많이 더워요..ㅠㅠ\n");
	printf("12345678123456781234567812345678\n");
	printf("탭\t이동확인\t!\n");
	printf("왼쪽으로 한칸 이동 12\b345\n");
	printf("라인의 처음으로 이동\r\nab\n");


}





























